export function getIsValidating(state) {
    return state.form.resetPassword.isValidating;
  }
  
  export function getIsLoading(state) {
    return state.form.resetPassword.isLoading;
  }
  
  export function getIsSubmitted(state) {
    return state.form.resetPassword.isSubmitted;
  }
  
  export function getIsSuccess(state) {
    return state.form.resetPassword.success;
  }
  